def change_direction(old_direction):
    directions = ['right', 'down', 'left',  'up']
    index = directions.index(old_direction)
    if index == 3:
        return directions[0]
    else:
        return directions[index+1]


n, m = [int(item) for item in input().split()]
my_matrix = []
for i in range(n):
    for j in range(m):
        my_matrix.append([0]*m)

count_r = 0
count_d = 0
count_l = 0
count_u = 0

i, j = 0, 0
current_direction = 'right'
if m == 1:
    for i in range(1, n+1):
        print(i)
else:
    for k in range(1, n*m+1):
        my_matrix[i][j] = k
        # print(f"k={k} my_matrix[{i}][{j}]={my_matrix[i][j]}")

        if current_direction == 'right':
            if 0 <= j+1 < m-1 and (my_matrix[i][j+1] == 0):
                j += 1
            else:
                if count_r == 0:
                    j += 1
                    count_r += 1
                if my_matrix[i][j] > 0:
                    i += 1
                current_direction = change_direction(current_direction)

        elif current_direction == 'down':
            if 0 <= i+1 < n-1 and (my_matrix[i+1][j] == 0):
                i += 1
            else:
                if count_d == 0:
                    i += 1
                    count_d += 1
                if my_matrix[i][j] > 0:
                    j -= 1
                current_direction = change_direction(current_direction)

        elif current_direction == 'left':
            if m > j-1 >= 1 and (my_matrix[i][j-1] == 0):
                j -= 1
            else:
                if count_l == 0:
                    j -= 1
                    count_l += 1
                if my_matrix[i][j] > 0:
                    i -= 1
                current_direction = change_direction(current_direction)

        elif current_direction == 'up':
            if n > i-1 >= 1 and (my_matrix[i-1][j] == 0):
                i -= 1
            else:
                if count_u == 0:
                    j += 1
                    count_u += 1
                if my_matrix[i][j] > 0:
                    j += 1
                current_direction = change_direction(current_direction)

    for i in range(n):
        for j in range(m):
            print(str(my_matrix[i][j]).ljust(3), end=' ')
        print()


