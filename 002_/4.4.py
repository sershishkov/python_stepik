    for j in range(n):
        if (i < j and i < n-1-j):
            sum_top += my_matrix[i][j]
        elif (i < j and i > n-1-j):
            sum_right += my_matrix[i][j]
        elif (i > j and i > n-1-j):
            sum_bottom += my_matrix[i][j]
        elif (i > j and i < n-1-j):
            sum_left += my_matrix[i][j]

